export function convertEmoji(text) {
    return text.replace(/:([a-z0-9_+-]+):/gi, (match, code) => {
        return EMOJI_MAP[code.toLowerCase()] || match;
    });
}

export function getEmoji(code) {
    return EMOJI_MAP[code.toLowerCase()] || null;
}

export function parseInlineFormatting(text) {
    const constructs = [];
    
    const boldItalicRegex = /(\*\*\*|___)(.+?)\1/g;
    const boldRegex = /(\*\*|__)(?=\S)(.+?)(?<=\S)\1/g;
    const italicRegex = /(\*|_)(?=\S)(.+?)(?<=\S)\1/g;
    const strikeRegex = /~~(.+?)~~/g;
    const codeRegex = /`([^`]+)`/g;
    const linkRegex = /\[([^\]]+)\]\(([^)]+)\)/g;
    const imageRegex = /!\[([^\]]*)\]\(([^)]+)\)/g;
    const inlineMathRegex = /\$([^\$]+)\$/g;
    const emojiRegex = /:([a-z0-9_+-]+):/gi;
    
    let match;
    
    while ((match = imageRegex.exec(text)) !== null) {
        constructs.push({
            type: 'image',
            from: match.index,
            to: match.index + match[0].length,
            alt: match[1],
            url: match[2],
            full: match[0]
        });
    }
    
    while ((match = linkRegex.exec(text)) !== null) {
        const overlaps = constructs.some(c => 
            (match.index >= c.from && match.index < c.to) ||
            (match.index + match[0].length > c.from && match.index + match[0].length <= c.to)
        );
        if (!overlaps) {
            constructs.push({
                type: 'link',
                from: match.index,
                to: match.index + match[0].length,
                text: match[1],
                url: match[2],
                full: match[0]
            });
        }
    }
    
    while ((match = codeRegex.exec(text)) !== null) {
        const overlaps = constructs.some(c => 
            (match.index >= c.from && match.index < c.to) ||
            (match.index + match[0].length > c.from && match.index + match[0].length <= c.to)
        );
        if (!overlaps) {
            constructs.push({
                type: 'code',
                from: match.index,
                to: match.index + match[0].length,
                content: match[1],
                full: match[0]
            });
        }
    }
    
    while ((match = boldItalicRegex.exec(text)) !== null) {
        const overlaps = constructs.some(c => 
            (match.index >= c.from && match.index < c.to) ||
            (match.index + match[0].length > c.from && match.index + match[0].length <= c.to)
        );
        if (!overlaps) {
            constructs.push({
                type: 'boldItalic',
                from: match.index,
                to: match.index + match[0].length,
                content: match[2],
                marker: match[1],
                full: match[0]
            });
        }
    }
    
    while ((match = boldRegex.exec(text)) !== null) {
        const overlaps = constructs.some(c => 
            (match.index >= c.from && match.index < c.to) ||
            (match.index + match[0].length > c.from && match.index + match[0].length <= c.to)
        );
        if (!overlaps) {
            constructs.push({
                type: 'bold',
                from: match.index,
                to: match.index + match[0].length,
                content: match[2],
                marker: match[1],
                full: match[0]
            });
        }
    }
    
    while ((match = italicRegex.exec(text)) !== null) {
        const overlaps = constructs.some(c => 
            (match.index >= c.from && match.index < c.to) ||
            (match.index + match[0].length > c.from && match.index + match[0].length <= c.to)
        );
        if (!overlaps && match[2].length > 0) {
            constructs.push({
                type: 'italic',
                from: match.index,
                to: match.index + match[0].length,
                content: match[2],
                marker: match[1],
                full: match[0]
            });
        }
    }
    
    while ((match = strikeRegex.exec(text)) !== null) {
        const overlaps = constructs.some(c => 
            (match.index >= c.from && match.index < c.to) ||
            (match.index + match[0].length > c.from && match.index + match[0].length <= c.to)
        );
        if (!overlaps) {
            constructs.push({
                type: 'strikethrough',
                from: match.index,
                to: match.index + match[0].length,
                content: match[1],
                full: match[0]
            });
        }
    }
    
    while ((match = inlineMathRegex.exec(text)) !== null) {
        const overlaps = constructs.some(c => 
            (match.index >= c.from && match.index < c.to) ||
            (match.index + match[0].length > c.from && match.index + match[0].length <= c.to)
        );
        if (!overlaps) {
            constructs.push({
                type: 'math',
                from: match.index,
                to: match.index + match[0].length,
                content: match[1],
                full: match[0]
            });
        }
    }
    
    while ((match = emojiRegex.exec(text)) !== null) {
        const emoji = getEmoji(match[1]);
        if (emoji) {
            const overlaps = constructs.some(c => 
                (match.index >= c.from && match.index < c.to) ||
                (match.index + match[0].length > c.from && match.index + match[0].length <= c.to)
            );
            if (!overlaps) {
                constructs.push({
                    type: 'emoji',
                    from: match.index,
                    to: match.index + match[0].length,
                    code: match[1],
                    emoji: emoji,
                    full: match[0]
                });
            }
        }
    }
    
    constructs.sort((a, b) => a.from - b.from);
    
    return constructs;
}

export function isPositionInConstruct(pos, construct) {
    return pos >= construct.from && pos <= construct.to;
}

export function getConstructAtPosition(constructs, pos) {
    return constructs.find(c => isPositionInConstruct(pos, c));
}

export function parseCodeBlockStart(line) {
    const match = line.match(/^```(\w*)$/);
    if (match) {
        return { isStart: true, lang: match[1] || 'text' };
    }
    return null;
}

export function parseCodeBlockEnd(line) {
    return line.trim() === '```';
}

export function parseMathBlockStart(line) {
    return line.trim() === '$$';
}

export function parseMermaidBlock(lines, startLine) {
    if (!lines[startLine] || lines[startLine].trim() !== '```mermaid') {
        return null;
    }
    
    let endLine = -1;
    let content = '';
    
    for (let i = startLine + 1; i < lines.length; i++) {
        if (lines[i].trim() === '```') {
            endLine = i;
            break;
        }
        content += lines[i] + '\n';
    }
    
    if (endLine === -1) {
        return null;
    }
    
    return {
        startLine,
        endLine,
        content: content.trim()
    };
}
